def tile(rows):
    plane = []
    for row in rows:
        bits = "0" + row + "00"
        plane.append(int(bits, 2))
    plane.append(0)
    return bytes(plane + [0] * 8)


def rotate_clockwise(rows):
    return [
        sum(((rows[7 - x] >> (7 - y)) & 1) << (7 - x) for x in range(8))
        for y in range(8)
    ]


