def tile(rows):
    plane = []
    for row in rows:
        bits = "0" + row + "00"
        plane.append(int(bits, 2))
    plane.append(0)
    return bytes(plane + [0] * 8)


def main():
    output = bytearray(8192)
    output[16:32] = bytes([0x7E, 0xFF, 0xDB, 0xFF, 0xFF, 0xDB, 0xFF, 0x7E] + [0] * 8)
